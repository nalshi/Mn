// المشكلة: كل التجار يشتركون بنفس الفرع (heads/main) بنفس المستودع.
// commitMultipleFiles تجلب sha آخر كوميت، تبني عليه tree جديد، ثم تحدّث
// الـ ref. لو صارت عمليتا مزامنة متزامنتين (نفس التاجر بتعديلين سريعين،
// أو حتى تاجرين مختلفين بنفس اللحظة)، كل وحدة تقرأ heads/main بشكل
// مستقل قبل ما توصل التعديل. فإذا انتهت عملية "أقدم" (بدأت قبل، بس
// تأخرت بالشبكة) بعد عملية أحدث، فهي تكتب فوق الـ ref بأساس (base_tree)
// قديم لا يحتوي آخر تعديل — والنتيجة: purge ينفّذ فعلاً (بعد كل كوميت
// ينجح)، لكن الكوميت المتأخر يمحو التعديل الأحدث بصمت بعد أن كان قد ظهر
// للحظة. هذا هو سبب "الـ purge يشتغل لكن التعديل ما يوصل".
//
// الحل: تسلسل كل عمليات المزامنة (قراءة قاعدة البيانات + بناء الملفات +
// commitMultipleFiles + purge) عبر طابور واحد بالذاكرة، بحيث كل عملية
// لا تبدأ (ولا حتى تقرأ قاعدة البيانات) إلا بعد اكتمال اللي قبلها تماماً.
// هذا يضمن أن قراءة قاعدة البيانات تصير وقت التنفيذ الفعلي لا وقت الطلب،
// فآخر عملية تنتهي دائماً تعكس أحدث حالة فعلية، ولا يوجد أساس (base_tree)
// قديم يُكتب فوق تعديل أحدث.
// ⚠️ ملاحظة: هذا يعمل داخل نفس isolate الخاص بالـ Worker. إذا احتجنا لاحقاً
// ضمان تسلسل عبر عدة isolates/instances، يلزم قفل خارجي (مثلاً KV أو
// Durable Object)، لكن هذا يغطي الحالة الشائعة (نفس الـ Worker instance)
// وهي غالبية الحالات العملية هنا.
// ========================================================

let tail = Promise.resolve();

export function enqueueSync(taskFn) {
  const result = tail.then(taskFn, taskFn);
  // نمسك أي رفض هنا فقط حتى لا تتوقف السلسلة عن باقي العمليات القادمة؛
  // الخطأ الفعلي لهذه العملية يصل للمستدعي عبر `result` نفسها (try/catch
  // بالدالة المستدعية).
  tail = result.then(
    () => undefined,
    () => undefined
  );
  return result;
}
